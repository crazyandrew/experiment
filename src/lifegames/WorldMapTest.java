	@Test
	public void testEvolveTest() {
		//等价类划分
		//只有一个，生变成死
		worldmap.evolveTest(1, 1);
		assertEquals(0,worldmap.getNextGeneration(1, 1));
		//只有一个，死依旧是死
		worldmap.evolveTest(4, 1);
		assertEquals(0,worldmap.getNextGeneration(4, 1));
		//只有两个，生依旧是生
		worldmap.evolveTest(1, 7);
		assertEquals(1,worldmap.getNextGeneration(1, 7));
		//只有两个，死依旧是死
		worldmap.evolveTest(1, 4);
		assertEquals(0,worldmap.getNextGeneration(1, 4));
		//只有三个，生依旧是生
		worldmap.evolveTest(4, 10);
		assertEquals(1,worldmap.getNextGeneration(4, 10));
		//只有三个，死变成生
		worldmap.evolveTest(1, 10);
		assertEquals(1,worldmap.getNextGeneration(1, 10));
		//四个，死依旧是死
		worldmap.evolveTest(4, 13);
		assertEquals(0,worldmap.getNextGeneration(4, 13));
		//四个，生变成死
		worldmap.evolveTest(1, 13);
		assertEquals(0,worldmap.getNextGeneration(1, 13));
		
		
		//判定 /条件覆盖
		worldmap.evolveTest(7, 0);
		assertEquals(0,worldmap.getNextGeneration(7, 0));
		worldmap.evolveTest(7, 49);
		assertEquals(0,worldmap.getNextGeneration(7, 49));
		worldmap.evolveTest(0, 40);
		assertEquals(0,worldmap.getNextGeneration(0, 40));
		worldmap.evolveTest(39, 40);
		assertEquals(0,worldmap.getNextGeneration(39,40));
		worldmap.evolveTest(7,4);
		assertEquals(0,worldmap.getNextGeneration(7,4));
		worldmap.evolveTest(1, 10);
		assertEquals(1,worldmap.getNextGeneration(1, 10));
		worldmap.evolveTest(1, 7);
		assertEquals(1,worldmap.getNextGeneration(1, 7));
	}

